/**
 * Enhanced Service Worker for HHR Hospital Tracker
 * Features:
 * - Offline fallback page
 * - Background sync for queued operations
 * - Intelligent caching strategies
 * - Local data persistence
 */

import { precacheAndRoute, cleanupOutdatedCaches } from 'workbox-precaching';
import { registerRoute, NavigationRoute, setCatchHandler } from 'workbox-routing';
import { StaleWhileRevalidate, CacheFirst, NetworkFirst, NetworkOnly } from 'workbox-strategies';
import { ExpirationPlugin } from 'workbox-expiration';
import { BackgroundSyncPlugin } from 'workbox-background-sync';
import { CacheableResponsePlugin } from 'workbox-cacheable-response';

// Version for cache busting
const CACHE_VERSION = 'v2.0.0';
const OFFLINE_PAGE = '/offline.html';

// Precache all static assets generated by Vite
precacheAndRoute(self.__WB_MANIFEST || []);

// Cleanup old caches
cleanupOutdatedCaches();

// ============================================
// OFFLINE FALLBACK
// ============================================

// Cache offline page on install
self.addEventListener('install', (event) => {
    event.waitUntil(
        caches.open(`offline-${CACHE_VERSION}`).then((cache) => {
            return cache.addAll([
                OFFLINE_PAGE,
                '/',
                '/index.html',
            ]);
        })
    );
    self.skipWaiting();
});

// Activate and claim clients immediately
self.addEventListener('activate', (event) => {
    event.waitUntil(
        Promise.all([
            self.clients.claim(),
            // Delete old caches
            caches.keys().then((cacheNames) => {
                return Promise.all(
                    cacheNames
                        .filter((name) => !name.includes(CACHE_VERSION))
                        .map((name) => caches.delete(name))
                );
            }),
        ])
    );
});

// Navigation fallback to offline page
setCatchHandler(async ({ event }) => {
    if (event.request.mode === 'navigate') {
        return caches.match(OFFLINE_PAGE);
    }
    return Response.error();
});

// ============================================
// CACHING STRATEGIES
// ============================================

// App Shell - Cache First (critical for offline)
registerRoute(
    ({ request }) =>
        request.destination === 'document' ||
        request.destination === 'script' ||
        request.destination === 'style',
    new StaleWhileRevalidate({
        cacheName: `app-shell-${CACHE_VERSION}`,
        plugins: [
            new CacheableResponsePlugin({
                statuses: [0, 200],
            }),
            new ExpirationPlugin({
                maxEntries: 60,
                maxAgeSeconds: 24 * 60 * 60, // 24 hours
            }),
        ],
    })
);

// Google Fonts - Cache First (long-lived)
registerRoute(
    ({ url }) =>
        url.origin === 'https://fonts.googleapis.com' ||
        url.origin === 'https://fonts.gstatic.com',
    new CacheFirst({
        cacheName: `google-fonts-${CACHE_VERSION}`,
        plugins: [
            new CacheableResponsePlugin({
                statuses: [0, 200],
            }),
            new ExpirationPlugin({
                maxEntries: 30,
                maxAgeSeconds: 365 * 24 * 60 * 60, // 1 year
            }),
        ],
    })
);

// Images - Cache First
registerRoute(
    ({ request }) => request.destination === 'image',
    new CacheFirst({
        cacheName: `images-${CACHE_VERSION}`,
        plugins: [
            new CacheableResponsePlugin({
                statuses: [0, 200],
            }),
            new ExpirationPlugin({
                maxEntries: 100,
                maxAgeSeconds: 30 * 24 * 60 * 60, // 30 days
            }),
        ],
    })
);

// Firebase/Firestore - Network First (critical data, need fresh)
registerRoute(
    ({ url }) =>
        url.origin.includes('firestore.googleapis.com') ||
        url.origin.includes('firebase'),
    new NetworkFirst({
        cacheName: `firebase-${CACHE_VERSION}`,
        networkTimeoutSeconds: 10, // Fallback to cache after 10s
        plugins: [
            new CacheableResponsePlugin({
                statuses: [0, 200],
            }),
            new ExpirationPlugin({
                maxEntries: 50,
                maxAgeSeconds: 60 * 60, // 1 hour
            }),
        ],
    })
);

// ============================================
// BACKGROUND SYNC FOR OFFLINE OPERATIONS
// ============================================

// Queue for failed patient updates
const patientSyncQueue = new BackgroundSyncPlugin('patientSyncQueue', {
    maxRetentionTime: 24 * 60, // Retry for up to 24 hours (in minutes)
    onSync: async ({ queue }) => {
        let entry;
        while ((entry = await queue.shiftRequest())) {
            try {
                await fetch(entry.request.clone());
                console.log('[SW] Synced:', entry.request.url);
            } catch (error) {
                console.error('[SW] Sync failed:', error);
                await queue.unshiftRequest(entry);
                throw error;
            }
        }
    },
});

// API calls with background sync
registerRoute(
    ({ url }) => url.pathname.startsWith('/api/'),
    new NetworkOnly({
        plugins: [patientSyncQueue],
    }),
    'POST'
);

// API GET requests - Stale While Revalidate
registerRoute(
    ({ url }) => url.pathname.startsWith('/api/'),
    new StaleWhileRevalidate({
        cacheName: `api-${CACHE_VERSION}`,
        plugins: [
            new ExpirationPlugin({
                maxEntries: 50,
                maxAgeSeconds: 5 * 60, // 5 minutes
            }),
        ],
    }),
    'GET'
);

// ============================================
// NAVIGATION
// ============================================

// Pages - Network First with offline fallback
registerRoute(
    ({ request }) => request.mode === 'navigate',
    new NetworkFirst({
        cacheName: `pages-${CACHE_VERSION}`,
        networkTimeoutSeconds: 5,
        plugins: [
            new ExpirationPlugin({
                maxEntries: 25,
            }),
        ],
    })
);

// ============================================
// MESSAGE HANDLING
// ============================================

self.addEventListener('message', (event) => {
    if (event.data) {
        switch (event.data.type) {
            case 'SKIP_WAITING':
                self.skipWaiting();
                break;

            case 'CACHE_URLS':
                // Allow app to request caching of specific URLs
                if (event.data.urls) {
                    caches.open(`dynamic-${CACHE_VERSION}`).then((cache) => {
                        cache.addAll(event.data.urls);
                    });
                }
                break;

            case 'CLEAR_CACHE':
                // Clear all caches
                caches.keys().then((names) => {
                    names.forEach((name) => caches.delete(name));
                });
                break;
        }
    }
});

// ============================================
// PUSH NOTIFICATIONS (future use)
// ============================================

self.addEventListener('push', (event) => {
    if (event.data) {
        const data = event.data.json();
        const options = {
            body: data.body || 'Nueva notificaciÃ³n',
            icon: '/images/icon-192.png',
            badge: '/images/icon-192.png',
            vibrate: [100, 50, 100],
            data: {
                dateOfArrival: Date.now(),
                primaryKey: 1,
            },
        };
        event.waitUntil(
            self.registration.showNotification(data.title || 'HHR Hospital', options)
        );
    }
});

self.addEventListener('notificationclick', (event) => {
    event.notification.close();
    event.waitUntil(
        clients.openWindow('/')
    );
});

console.log(`[SW] Service Worker ${CACHE_VERSION} loaded`);

